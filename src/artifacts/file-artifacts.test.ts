    junkPaths.forEach((p, i) => writeFileSync(p, Buffer.from([0x50, 0x4b, 0x03, i])));
    writeFileSync(reportPath, '%PDF-1.4\nreport bytes');
    writeFileSync(shelfPngPath, Buffer.from([0x89, 0x50, 0x4e, 0x47]));
    const future = new Date(Date.now() + 2_000);
    for (const p of [...junkPaths, reportPath, shelfPngPath]) utimesSync(p, future, future);

    const events: ArtifactEvent[] = [];
    const coordinator = new ArtifactCoordinator('turn-optin-scan', (event) => events.push(event));
    const tracker = createTurnFileArtifactTracker({ artifactCoordinator: coordinator });

    await tracker.scanAndEmit();

    const filenames = events
      .filter((event): event is Extract<ArtifactEvent, { type: 'open' }> => event.type === 'open')
      .map((event) => String(event.artifact.data.filename))
      .sort();
    // The cwd-report backstop survives; the shelf mints everything recognized.
    expect(filenames).toEqual(['report.pdf', 'shelf-chart.png']);
