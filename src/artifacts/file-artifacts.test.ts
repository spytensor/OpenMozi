  describe('cross-turn artifact identity', () => {
    it('does not republish a file an earlier turn of the session already published', async () => {
      // The production shape: a plan hands the deliverable to a background turn,
      // which generates and publishes the PDF; the foreground turn's end-of-turn
      // scan then finds the same file on the shared output dir. Both minted their
      // own artifact id and the user saw the same 33KB document twice.
      const outputDir = getOutputDir();
      mkdirSync(outputDir, { recursive: true });
      const pdfPath = join(outputDir, 'US_Macro_Bond_Report.pdf');
      writeFileSync(pdfPath, '%PDF-1.4\nreport bytes');
      const future = new Date(Date.now() + 2_000);
      utimesSync(pdfPath, future, future);

      const events: ArtifactEvent[] = [];
      const tracker = createTurnFileArtifactTracker({
        artifactCoordinator: new ArtifactCoordinator('turn-foreground', (event) => events.push(event)),
        // Stands in for the durable timeline: the background turn's card.
        resolvePublishedArtifactId: (path) => (path === realpathSync(pdfPath) ? 'file_from_background_turn' : null),
      });

      await tracker.scanAndEmit();

      // One card, not two: this turn takes over the background turn's artifact
      // instead of minting a second id for the same file.
      expect(events.filter((e) => e.type === 'open')).toHaveLength(0);
      const patches = events.filter((e): e is Extract<ArtifactEvent, { type: 'patch' }> => e.type === 'patch');
      expect(patches.map((p) => p.artifactId)).toContain('file_from_background_turn');
    });

    it('still publishes a file no earlier turn produced', async () => {
      const outputDir = getOutputDir();
      mkdirSync(outputDir, { recursive: true });
      const pdfPath = join(outputDir, 'fresh_report.pdf');
      writeFileSync(pdfPath, '%PDF-1.4\nfresh');
      const future = new Date(Date.now() + 2_000);
      utimesSync(pdfPath, future, future);

      const events: ArtifactEvent[] = [];
      const tracker = createTurnFileArtifactTracker({
        artifactCoordinator: new ArtifactCoordinator('turn-fresh', (event) => events.push(event)),
        resolvePublishedArtifactId: () => null,
      });

      await tracker.scanAndEmit();

      const opens = events.filter((e): e is Extract<ArtifactEvent, { type: 'open' }> => e.type === 'open');
      expect(opens.map((e) => String(e.artifact.data.filename))).toEqual(['fresh_report.pdf']);
    });

    it('updates the existing card when a later turn regenerates the same path', async () => {
      // The case suppression got wrong: turn 2 regenerates report.pdf. Skipping
      // it left that turn showing no card at all. Adopting patches the card the
      // earlier turn published — one card, and it reflects the new content.
      const outputDir = getOutputDir();
      mkdirSync(outputDir, { recursive: true });
      const pdfPath = join(outputDir, 'owned_report.pdf');
      const events: ArtifactEvent[] = [];
      const tracker = createTurnFileArtifactTracker({
        artifactCoordinator: new ArtifactCoordinator('turn-two', (event) => events.push(event)),
        resolvePublishedArtifactId: () => 'file_from_turn_one',
      });

      writeFileSync(pdfPath, '%PDF-1.4\nv2 regenerated with Q3 numbers');
      utimesSync(pdfPath, new Date(Date.now() + 2_000), new Date(Date.now() + 2_000));
      await tracker.scanAndEmit();

      // No second card...
      expect(events.filter((e) => e.type === 'open')).toHaveLength(0);
      // ...but the first turn's card is updated rather than left stale or silent.
      const patches = events.filter((e): e is Extract<ArtifactEvent, { type: 'patch' }> => e.type === 'patch');
      expect(patches.map((p) => p.artifactId)).toContain('file_from_turn_one');
      expect(patches.at(-1)?.patch.data).toMatchObject({ filename: 'owned_report.pdf' });
    });
  });
