    // The render batch stays durable but collapses behind the deliverable — it
    // is no longer dropped outright, so the user can still reach it.
    for (const frame of ['slide-01.jpg', 'slide-02.jpg', 'slide-03.jpg']) {
      expect(roleOf(frame), frame).toBe('supporting');
    }
