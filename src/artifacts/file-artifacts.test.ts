    writeFileSync(pdfPath, '%PDF-1.4\npdf bytes');
    // Distinct bytes per frame: byte-identical files are intentionally deduped
    // to a single artifact within a turn, which would mask the role assertion.
    slidePaths.forEach((p, i) => writeFileSync(p, Buffer.from([0xff, 0xd8, 0xff, i])));
    const future = new Date(Date.now() + 2_000);
