  // the Brain rm -f'd a delivered PDF 39s after its card was emitted). A card
  // whose file vanishes is patched missing:true through the coordinator; if
  // the file reappears at the same path, the flag clears the same way.
  it('marks a carded deliverable missing when its file vanishes, and clears on reappearance', async () => {
    const outputDir = join(moziHome, 'output');
    mkdirSync(outputDir, { recursive: true });
    const pdfPath = join(outputDir, 'report.pdf');
    writeFileSync(pdfPath, '%PDF-1.4\ndelivered bytes');
    const future = new Date(Date.now() + 2_000);
    utimesSync(pdfPath, future, future);

    const artifactEvents: ArtifactEvent[] = [];
    const coordinator = new ArtifactCoordinator('turn-liveness', (event) => artifactEvents.push(event));
    const tracker = createTurnFileArtifactTracker({ artifactCoordinator: coordinator });

    await tracker.scanAndEmit();
    const carded = artifactEvents.find(
      (event) => event.type === 'open' && (event.artifact.data as { filename?: string }).filename === 'report.pdf',
    );
    expect(carded).toBeTruthy();

    const lastPatchData = () => {
      const patch = artifactEvents.filter((event) => event.type === 'patch').at(-1);
      return (patch && patch.type === 'patch' ? patch.patch.data : undefined) as { missing?: boolean } | undefined;
    };

    rmSync(pdfPath);
    await tracker.scanAndEmit();
    expect(lastPatchData()?.missing).toBe(true);

    writeFileSync(pdfPath, '%PDF-1.4\nregenerated bytes');
    utimesSync(pdfPath, future, future);
    await tracker.scanAndEmit();
    // EXPLICIT false, never an absent key: store and client merge patch data
    // over existing data, so a missing key cannot clear a persisted `true`
    // (review finding 2026-07-19 — key-deletion left regenerated files
    // labeled "removed" forever).
    expect(lastPatchData()?.missing).toBe(false);
    expect((lastPatchData() as { filename?: string } | undefined)?.filename).toBe('report.pdf');
  });

