#!/usr/bin/env node
/**
 * Tier 1 acceptance — cross-tenant read via ?tenantId= (issue #334, item 1.2).
 *
 * Launches the REAL built runtime (dist/index.js) in auth_mode=token with an
 * isolated MOZI_HOME, mints a JWT for tenant "tenant-a" (viewer), then uses
 * Playwright's request API to call tenant/dashboard endpoints while trying to
 * smuggle `?tenantId=tenant-b`.
 *
 * The endpoints echo the tenant they served (getTenantUsage → { tenant_id }).
 *
 * PASS = server ignores ?tenantId= and serves the AUTHENTICATED tenant (tenant-a).
 * FAIL = server honors ?tenantId=tenant-b => cross-tenant read (vulnerable build).
 *
 * Real Playwright + real running instance, no mocks.
 */
import { spawn } from 'node:child_process';
import { mkdtempSync, writeFileSync, mkdirSync, readFileSync, existsSync, rmSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { createServer } from 'node:net';
import { createHmac, randomUUID } from 'node:crypto';
import { request } from 'playwright';

const host = '127.0.0.1';

function b64url(input) {
  return Buffer.from(input).toString('base64').replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '');
}
// Mirrors src/security/jwt.ts sign() exactly (HS256).
function mintToken(secret, sub, tenantId, roles) {
  const header = b64url(JSON.stringify({ alg: 'HS256', typ: 'JWT' }));
  const now = Math.floor(Date.now() / 1000);
  const payload = b64url(JSON.stringify({ sub, iat: now, exp: now + 3600, jti: randomUUID(), tenant_id: tenantId, roles }));
  const data = `${header}.${payload}`;
  const sig = createHmac('sha256', secret).update(data).digest('base64').replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '');
  return `${data}.${sig}`;
}

function getFreePort() {
  return new Promise((resolve, reject) => {
    const srv = createServer();
    srv.unref();
    srv.on('error', reject);
    srv.listen(0, host, () => {
      const port = srv.address().port;
      srv.close(() => resolve(port));
    });
  });
}

async function waitForHealth(baseUrl, proc, timeoutMs = 30000) {
  const start = Date.now();
  let lastErr;
  while (Date.now() - start < timeoutMs) {
    if (proc.exitCode !== null) throw new Error(`runtime exited early (code ${proc.exitCode})`);
    try {
      const r = await fetch(`${baseUrl}/api/health`);
      if (r.ok) return await r.json();
    } catch (e) { lastErr = e; }
    await new Promise((res) => setTimeout(res, 400));
  }
  throw new Error(`timeout waiting for health: ${lastErr?.message ?? 'unknown'}`);
}

async function main() {
  const moziHome = mkdtempSync(join(tmpdir(), 'mozi-tenant-iso-'));
  const workspaceDir = join(moziHome, 'workspace');
  mkdirSync(workspaceDir, { recursive: true });
  const port = await getFreePort();
  const baseUrl = `http://${host}:${port}`;

  writeFileSync(join(moziHome, 'mozi.json'), JSON.stringify({
    server: { host, port, auth_mode: 'token' },
    brain: { model: 'gpt-4.1-mini' },
    model_router: { brain_provider: 'openai' },
    providers: { openai: { apikey: 'tenant-iso-smoke-key' } },
    telegram: { bot_token: '' },
    wechat: { bot_token: '' },
    workspace: { dir: workspaceDir },
    tools: { fs: { workspace_only: true, additional_allowed_roots: [moziHome] }, subagents: { enabled: false } },
  }, null, 2));

  const env = {
    ...process.env,
    NODE_NO_WARNINGS: '1',
    MOZI_HOME: moziHome,
    MOZI_SERVER_HOST: host,
    MOZI_SERVER_PORT: String(port),
    MOZI_SERVER_AUTH_MODE: 'token',
    MOZI_PROJECT_ROOT: process.cwd(),
    TELEGRAM_BOT_TOKEN: '',
    WECHAT_BOT_TOKEN: '',
  };
  const proc = spawn(process.execPath, ['dist/index.js'], { cwd: process.cwd(), env, stdio: ['ignore', 'pipe', 'pipe'] });
  const logs = [];
  proc.stdout.on('data', (c) => logs.push(c.toString()));
  proc.stderr.on('data', (c) => logs.push(c.toString()));

  let ctx;
  let failed = false;
  const problems = [];
  try {
    await waitForHealth(baseUrl, proc);

    const secretPath = join(moziHome, 'jwt-secret');
    if (!existsSync(secretPath)) throw new Error('jwt-secret not generated by runtime');
    const secret = readFileSync(secretPath, 'utf-8').trim();
    const tokenA = mintToken(secret, 'user-a', 'tenant-a', ['viewer']);

    ctx = await request.newContext({ baseURL: baseUrl, extraHTTPHeaders: { Authorization: `Bearer ${tokenA}` } });

    // Sanity: token is accepted (not 401).
    const sanity = await ctx.get('/api/tenant/usage');
    if (sanity.status() === 401 || sanity.status() === 403) {
      throw new Error(`tenant-a token rejected (${sanity.status()}): ${await sanity.text()}`);
    }
    const own = await sanity.json();
    if (own.tenant_id !== 'tenant-a') {
      problems.push(`baseline /api/tenant/usage should serve tenant-a, got ${own.tenant_id}`);
    }

    // The attack: tenant-a asks for tenant-b's data via query param.
    const cases = [
      { path: '/api/tenant/usage?tenantId=tenant-b', field: 'tenant_id', label: 'tenant/usage' },
    ];
    for (const c of cases) {
      const res = await ctx.get(c.path);
      const body = await res.json();
      const served = body[c.field];
      if (served === 'tenant-b') {
        failed = true;
        problems.push(`LEAK: ${c.label} honored ?tenantId=tenant-b and served tenant-b's data (attacker-controlled)`);
      } else if (served !== 'tenant-a') {
        problems.push(`${c.label} served unexpected tenant "${served}" (expected tenant-a)`);
      }
    }

    if (failed || problems.length) {
      console.error('FAIL tier1-tenant-isolation:');
      for (const p of problems) console.error('  - ' + p);
      process.exitCode = 1;
    } else {
      console.log('PASS tier1-tenant-isolation: ?tenantId=tenant-b ignored; server served authenticated tenant-a only');
      console.log('  exercised: real runtime (auth_mode=token) + Playwright request with tenant-a Bearer JWT');
    }
  } catch (err) {
    console.error('ERROR tier1-tenant-isolation:', err?.message || err);
    console.error('--- runtime logs (tail) ---\n' + logs.join('').split('\n').slice(-25).join('\n'));
    process.exitCode = 2;
  } finally {
    if (ctx) await ctx.dispose().catch(() => {});
    proc.kill('SIGTERM');
    await new Promise((res) => setTimeout(res, 500));
    if (proc.exitCode === null) proc.kill('SIGKILL');
    rmSync(moziHome, { recursive: true, force: true });
  }
}

main();
